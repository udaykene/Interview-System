function formatTestCases(entries) {
  return entries.map(([input, expectedOutput, isHidden]) => ({
    input,
    expectedOutput,
    isHidden,
  }));
}

async function main() {
  const inputPath = path.resolve(process.argv[2] || defaultInput);
  const outputPath = path.resolve(process.argv[3] || defaultOutput);

  const problems = JSON.parse(await fs.readFile(inputPath, "utf8"));

  for (const problem of problems) {
    const replacement = testsBySlug[problem.slug];
    if (!replacement) {
      throw new Error(`No replacement test cases defined for slug: ${problem.slug}`);
    }
    problem.testCases = formatTestCases(replacement);
  }

  await fs.writeFile(outputPath, `${JSON.stringify(problems, null, 2)}\n`, "utf8");
  console.log(`Repaired bulk import written to ${outputPath}`);
}

main().catch((error) => {
  console.error("Failed to repair bulk import:", error.message);
  process.exit(1);
});
